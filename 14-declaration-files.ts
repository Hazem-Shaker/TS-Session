//-----------------------------1-----------------------------
//-----------------------------What Are .d.ts Files?-----------------------------
/*
--> Declaration files (.d.ts) contain ONLY type information — no runtime code.
    They describe the shape of JavaScript code so TypeScript can type-check it.

    Where they come from:
      1. Auto-generated by tsc when `declaration: true` in tsconfig (see /dist/*.d.ts)
      2. Bundled inside npm packages (look for "types" field in package.json)
      3. Installed separately via @types packages (DefinitelyTyped)
      4. Written manually for untyped JS code
*/

//-----------------------------2-----------------------------
//-----------------------------Reading @types Packages-----------------------------
/*
--> @types packages live in node_modules/@types/<package>/
    They contain .d.ts files that TS loads automatically.

    Example: @types/express
      node_modules/@types/express/index.d.ts contains:
        - interface Request { ... }
        - interface Response { ... }
        - function express(): Express;

    How TS finds them:
      1. "types" or "typeRoots" in tsconfig.json
      2. By default, TS includes all @types/* in node_modules
      3. Our tsconfig has "types": [] — meaning NO auto-inclusion,
         so we'd need to add e.g. "types": ["node"] to use @types/node

    To install:
      npm i -D @types/express    // types for express
      npm i -D @types/node       // types for Node.js built-ins
*/

