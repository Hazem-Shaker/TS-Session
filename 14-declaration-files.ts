//-----------------------------8-----------------------------
//-----------------------------Summary: tsconfig Keys for Declarations-----------------------------
/*
  | tsconfig option     | Purpose                                          |
  |---------------------|--------------------------------------------------|
  | "declaration"       | Generate .d.ts files alongside .js output        |
  | "declarationMap"    | Generate .d.ts.map for Go-to-Definition in IDEs  |
  | "types"             | Which @types packages to auto-include            |
  | "typeRoots"         | Folders to search for type declarations           |
  | "skipLibCheck"      | Skip type-checking .d.ts files (faster builds)   |
*/
