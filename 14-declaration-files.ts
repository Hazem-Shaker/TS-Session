//-----------------------------Module Augmentation-----------------------------
/*
--> You can ADD new properties to existing types from third-party packages.
    This uses declaration merging on the module's interfaces.
*/

// Example: adding a custom property to Express Request
// In a file like: express.d.ts
//
// import "express";
// declare module "express" {
//   interface Request {
//     userId?: string;
