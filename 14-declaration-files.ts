// }
//
// Now req.userId and req.role are recognized in all Express handlers.

//-----------------------------6-----------------------------
//-----------------------------Global Declarations (declare global)-----------------------------
/*
--> `declare global` lets you add types to the global scope FROM a module file.
    Any file with a top-level import or export is a module.
    Inside `declare global { }` you can:
      - Extend existing global interfaces (Window, globalThis, NodeJS.Process…)
      - Add brand-new global variables, functions, or types
    The block only affects types — it doesn't create runtime values.
*/

// Step 1: Extend the global scope with custom types
declare global {
  // Add properties to the browser's Window interface
  interface Window {
    __APP_VERSION__: string;
    analytics: { track(event: string, data?: Record<string, unknown>): void };
  }

  // Declare a global variable that exists at runtime (e.g. injected by a script tag)
  var APP_ENV: "development" | "staging" | "production";

  // Declare a global function (e.g. exposed by a legacy script)
  function showToast(message: string, duration?: number): void;

  // Add a brand-new global interface accessible anywhere without import
  interface RuntimeConfig {
    apiUrl: string;
    featureFlags: Record<string, boolean>;
  }
}

// Step 2: Now use them — TS recognises these globally

// Window properties
window.__APP_VERSION__ = "2.1.0";
window.analytics.track("page_view", { page: "/home" });

// Global variable
console.log(APP_ENV); // "development" | "staging" | "production"

// Global function
showToast("Settings saved!", 3000);

// Global interface (no import needed)
const runtimeConfig: RuntimeConfig = {
  apiUrl: "https://api.example.com",
  featureFlags: { darkMode: true, betaSearch: false },
};

/*
--> When to use `declare global`:
    1. Your app injects globals at startup (env vars, config, analytics SDKs)
    2. You're augmenting Window / NodeJS.ProcessEnv for environment variables
    3. You're writing a library that attaches to globalThis

--> Common real-world pattern: typing process.env
*/

declare global {
  namespace NodeJS {
    interface ProcessEnv {
      DATABASE_URL: string;
      JWT_SECRET: string;
      PORT?: string;
    }
  }
}

